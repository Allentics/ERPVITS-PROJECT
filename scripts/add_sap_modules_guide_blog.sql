            </p>
            <p>Core features of the sap mm module encompass:</p>
            <ul>
                <li>Management of the company''s master data related to materials and vendors</li>
                <li>Managing requests to purchase and the orders</li>
                <li>Managing the receipts of goods and the documentation of invoices</li>
                <li>Management of the inventories and the warehouses the company owns</li>
                <li>Valuation of materials and determining of the accounts related to them</li>
                <li>Management of the evaluation of the vendors and contracts with them</li>
                <li>Integration of the management of the materials with the planning of the requirements</li>
            </ul>

