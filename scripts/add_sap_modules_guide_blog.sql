            <h2 class="text-2xl font-bold mt-12 mb-6">What are SAP Modules?</h2>
            <p>
                SAP modules are system components that specialize in specific business processes within an organization. Each module functions independently yet integrates with other modules, creating a single system, with no data silos and an uninterrupted stream of information between every department. Each module has its own distinct capabilities.
            </p>
